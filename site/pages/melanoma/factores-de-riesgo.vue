    <h1>Factores de Riesgo del Melanoma.</h1>
    <p>
      Un factor de riesgo es todo aquello que aumenta el riesgo de padecer una
      enfermedad, en este caso el cáncer de piel. Existen factores de riesgo que
      sí podemos controlar y otros que no podemos, como por ejemplo la edad y el
      color de la piel, etc.
    </p>
    <p>
      Estos son los
      <a href="/documentos/FACTORES DE RIESGO DEL MELANOMA.pdf" target="_blank"
        >factores de riesgo</a
      >
      que pueden aumentar la probabilidad de padecer melanoma en algún momento
      de tu vida.
    </p>
    <h3>Piel muy blanca.</h3>
    <p>
      Tener menos pigmento (melanina) en la piel significa que tienes menos
      protección contra la radiación UV, por lo tanto estás más expuesto al daño
      causado por los rayos UVA y UVB. Si tienes cabello rubio o pelirrojo, ojos
      claros y pecas o te quemas fácilmente con el sol, es más probable que
      desarrolles un melanoma en comparación a alguien con tez más oscura, sin
      embargo, tampoco las personas con tez obscura están exentas de tener
      melanoma.
    </p>
    <h3>Antecedentes de quemaduras causadas por el sol.</h3>
    <p>
      Una o más quemaduras solares graves con ampollas pueden aumentar el riesgo
      de padecer melanoma. Según “American Cancer Society”, tener más de 5
      quemaduras causadas por el sol, pueden duplicar la probabilidad de padecer
      esta enfermedad en un futuro.
    </p>
    <h3>Camas de Bronceado.</h3>
    <p>
      Las personas que usan camas de bronceado y lámparas de sol, tienen un
      mayor riesgo de desarrollar cualquier tipo de cáncer de piel, incluyendo
      el melanoma.
    </p>
    <h3>Exposición al sol o a la luz ultravioleta (UV).</h3>
    <p>
      Este es el factor de riesgo principal en el melanoma, la luz solar es la
      fuente principal de la radiación ultravioleta. La exposición a la
      radiación UV, que proviene del sol, de las luces y camas de bronceado,
      puede aumentar el riesgo de padecer cáncer de piel y melanoma. Este es uno
      de los factor de riesgo que sí podemos controlar, en comparación con
      otros. Aunque los rayos UV representan sólo una pequeña porción de los
      rayos del sol, son los principales causantes de daño solar en la piel. Los
      rayos UV dañan el ADN (genes) dentro de las células de la piel. Los
      cánceres de piel pueden comenzar cuando este daño afecta el ADN de los
      genes que controlan el crecimiento de las células de la piel. El melanoma
      en el tronco (pecho y espalda) y las piernas ha sido vinculado a las
      quemaduras de sol frecuentes (especialmente en la niñez). Cabe aclarar que
      existen melanomas que no están directamente relacionados con la exposición
      al sol, como es el caso del melanoma lentiginoso acral y el melanoma de
      mucosa y ojos.
    </p>
    <h3>Vivir más cerca del ecuador o en una mayor altitud.</h3>
    <p>
      Las personas que viven más cerca del ecuador de la Tierra, donde los rayos
      solares son más directos, experimentan mayores cantidades de rayos UV, por
      lo tanto, existe más probabilidad de que padezcan melanoma. Además, si
      vives en una mayor altitud, también estás expuesto a más radiación UV.
    </p>
    <h3>Tener muchos lunares o lunares atípicos.</h3>
    <p>
      Un lunar (también conocido como nevus o nevo) es un tumor pigmentado
      benigno (no canceroso). Por lo general, los lunares no están presentes en
      los bebés al momento de nacer, sino que empiezan a aparecer en la infancia
      y cuando las personas llegan a ser adultos jóvenes. La presencia de más de
      50 lunares en el cuerpo indica un riesgo mayor de padecer melanoma.
      Además, tener un tipo inusual de lunar o lunar atípico, aumenta el riesgo
      de padecer la enfermedad. Conocidos médicamente como nevos displásicos,
      estos tienden a ser más grandes que los lunares normales y tienen bordes
      irregulares y una mezcla de colores. La mayoría de los lunares displásicos
      nunca se vuelven cancerosos, y muchos melanomas parecen originarse sin un
      lunar displásico preexistente.
    </p>
    <h3>Síndrome del nevo displásico (síndrome de lunar atípico).</h3>
    <p>
      Las personas con esta afección hereditaria tienen muchos lunares
      displásicos. Si al menos un pariente cercano ha tenido melanoma, esta
      afección se conoce como síndrome del melanoma de nevo múltiple atípico
      familiar y síndrome del melanoma, o FAMMM. Las personas con esta afección
      tienen un riesgo muy alto de por vida de padecer un melanoma, por lo que
      necesitan exámenes muy minuciosos y regulares de la piel realizados por un
      dermatólogo.
    </p>
    <h3>Antecedentes familiares de melanoma.</h3>
    <p>
      Si un familiar cercano o de primer grado, como un padre, una madre, un
      hijo o un hermano, ha tenido melanoma, también tienes una mayor
      probabilidad de desarrollar un melanoma. Aproximadamente 10% de todas las
      personas con melanoma tienen antecedentes familiares de esta enfermedad.
      El aumento en el riesgo pudiera deberse a que compartieron un estilo de
      vida de exposición frecuente al sol en la familia, una familia de piel muy
      blanca, ciertos cambios genéticos (mutaciones) que son más frecuentes en
      una familia, o una combinación de estos factores.
    </p>
    <h3>Antecedente personal de melanoma u otros cánceres de piel.</h3>
    <p>
      Una persona que ha padecido melanoma tiene un mayor riesgo de padecer
      melanoma nuevamente. Las personas que han tenido cáncer de piel de células
      basales o células escamosas también están en mayor riesgo de padecer
      melanoma.
    </p>
    <h3>Sistema inmune débil.</h3>
    <p>
      El sistema inmunológico de una persona ayuda a combatir cualquier tipo de
      cáncer. Las personas con sistemas inmunitarios debilitados (debido a
      ciertas enfermedades o tratamientos médicos) tienen más probabilidades de
      padecer muchos tipos de cáncer de piel, incluyendo melanoma. Por ejemplo,
      a las personas que reciben un trasplante de órgano generalmente se les
      administran medicamentos que debilitan su sistema inmunitario, a fin de
      ayudar a prevenir que el cuerpo rechace el nuevo órgano, sin embargo, esto
      puede aumentar el riesgo de padecer melanoma. Las personas infectadas con
      VIH, el virus que causa el SIDA, a menudo tienen sistemas inmunitarios
      debilitados y también están en mayor riesgo de padecer melanoma.
    </p>
    <h3>Algunas otras condiciones.</h3>
    <p>
      El xeroderma pigmentoso (XP) es un padecimiento hereditario que afecta la
      capacidad de las células de la piel de reparar el daño causado a su ADN.
      Las personas con XP tienen un alto riesgo de melanoma y otros cánceres de
      piel cuando son jóvenes, especialmente en áreas de la piel que han sido
      expuestas al sol.
    </p>
    <h3>Envejecimiento.</h3>
    <p>
      El melanoma es más probable que se presente en personas de edad avanzada
      debido a la acumulada exposición a los rayos UV a lo largo de la vida, sin
      embargo, el melanoma también afecta a personas más jóvenes. De hecho, el
      melanoma es uno de los cánceres más comunes en las personas que tienen
      menos de 30 años de edad.
    </p>
    <p>
      Presentar uno, o incluso muchos de los factores de riesgo anteriores, no
      significa que usted padecerá melanoma. Muchas personas con factores de
      riesgo nunca padecen melanoma. Además, algunas personas que llegan a
      padecer cáncer puede que tengan pocos o que no presenten factores de
      riesgo conocidos.
    </p>
    <p>
      Aun así, resulta importante informarse sobre los factores de riesgo para
      el melanoma, ya que podrían haber medidas que usted puede tomar para
      reducir el riesgo de padecerlo. Si usted presenta un mayor riesgo debido a
      ciertos factores, también hay medidas que puede tomar para ayudar a
      descubrir el cáncer en etapa temprana, cuando es probable que sea más
      fácil de tratar.
    </p>
