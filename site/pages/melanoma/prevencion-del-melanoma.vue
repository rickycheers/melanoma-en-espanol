    <h1>Prevención del Melanoma</h1>
    <p>
      No hay una forma segura de prevenir el melanoma, algunos factores de
      riesgo como la edad, raza y padecimientos en la familia, no se pueden
      controlar, sin embargo, hay cosas que podemos hacer que pueden disminuir
      el riesgo de tener melanoma u otro tipo de cáncer en la piel.
    </p>
    <h2>
      Esta es la guía de prevención del melanoma
      <a
        href="/documentos/Guía de Prevención del Melanoma.pdf"
        target="_blank"
      >
        <i class="fa fa-file-pdf ml-2"></i>
      </a>
    </h2>
    <h3>Limita tu exposición al sol y a los rayos ultravioleta (UV)</h3>
    <p>
      Este es una de las mejores formas que pueden ayudar a disminuir el riesgo
      de tener melanoma. Protégete del sol siempre que estés en el exterior.
    </p>
    <p>
      Algunas personas creen que sólo se debe utilizar protección solar cuando
      estamos en vacaciones, en alguna alberca o playa, sin embargo, debemos de
      protegernos todos los días.Una forma de hacerlo es utilizando bloqueador
      solar todos los días, re-aplicando cada 2 horas.
    </p>
    <p>
      <img src="/images/prevencion-1.png" loading="lazy" />
    </p>
    <h3>Busca la sombra cuando estás en el exterior.</h3>
    <p>
      Una forma obvia de limitar la exposición al sol es evitando estar en el
      exterior por un periodo largo de tiempo, especialmente entre las 10am y
      las 4 pm, horas en las que los rayos del sol son más potentes.
    </p>
    <p>
      Los rayos UV están presentes en la tierra durante todo el año, aún en días
      nublados, sin embargo hay lugares y temporadas en donde son más fuertes y
      peligrosos, especialmente debes cuidarte más cuando estás en la playa o en
      lugares con nieve, y esto se debe a que el agua, la arena y la nieve
      reflejan la luz del sol, aumentando la cantidad de rayos UV a los que
      estamos expuestos.
    </p>
    <p>
      En caso de estar en el exterior, protegerte del sol bajo la sombra es una
      de las mejores alternativas que debes seguir si quieres evitar la
      exposición a los rayos UV.
    </p>
    <p>
      <img src="/images/prevencion-2.png" loading="lazy" />
    </p>
    <h3>Protege tu piel con ropa</h3>
    <p>
      Si vas a estar en el sol utiliza ropa y accesorios como barrera de
      protección contra los rayos UV. Utiliza playeras de manga larga, sombreros
      de ala ancha, y lentes de sol como escudo protector.
    </p>
    <p>
      La ropa ofrece diferentes niveles de protección contra los rayos UV,
      dependiendo el tipo de tela y el diseño de la prenda. Las playeras de
      manga larga, los pantalones y faldas largas son aquellas prendas que
      protegen mejor la piel. Las prendas de color obscuro también ofrecen mejor
      protección, las telas con tejidos compactos minimizan la cantidad de rayos
      que atraviesan la ropa, y la tela seca protege más en comparación a la
      tela mojada.
    </p>
    <p>
      Cuando puedes ver la luz del sol a través de la tela, significa que no
      necesariamente está bloqueando los rayos UV.
    </p>
    <p>
      Existen empresas dedicadas a producir ropa protectora contra el sol. Este
      tipo de prendas tienen etiquetas en donde dice el factor de protección
      solar que tienen, así como en los bloqueadores solares.
    </p>
    <p>
      <img src="/images/prevencion-3.png" loading="lazy" />
    </p>
    <h3>Utiliza sombreros y sombrillas</h3>
    <p>
      Aún en el interior, los rayos UV pueden pasar a través de las ventanas de
      tu casa, oficina o coche. Normalmente la mayoría de las ventanas bloquean
      los rayos UVB, sin embargo, no bloquean la mayoría de los rayos UVA , es
      por esto que es importante proteger tu piel en todo momento, en especial
      cuando estas en el exterior.
    </p>
    <p>Utilizar sombrillas y sombreros puede ser un buen métodos de protección.</p>
    <p>
      Un sombrero de ala ancha es una opción ideal para proteger tu piel del
      sol, ya que protege tu cara, ojos, orejas, nariz, cuero cabelludo y
      cuello. Mientras más obscuro el color mejor.
    </p>
    <p>
      Si no tienes un sombrero de ala ancha, puedes hacerlo tú, utilizando tela
      o una bandana que protege tu piel debajo de la gorra normal.
    </p>
    <p>
      <img src="/images/prevencion-4.png" loading="lazy" />
    </p>
    <h3>Utiliza lentes de sol</h3>
    <p>
      Utilizar lentes de sol con protección solar es una buena estrategia para
      proteger nuestros ojos y la piel delicada que los rodea.
    </p>
    <p>
      Los lentes ideales deben bloquear el 99% o 100% de los rayos UVB y UVA.
      Fíjate en la etiqueta antes de comprarlos. No porque los lentes sean más
      obscuros significa, que protegen mejor, realmente el nivel de protección
      viene de un químico transparente que aplican a los lentes.
    </p>
    <p>
      <img src="/images/prevencion-5.png" loading="lazy" />
    </p>
    <h3>Evita broncearte, usar camas de bronceado o lámparas de sol.</h3>
    <p>
      Una piel bronceada no es una piel sana, más bien son tus células
      defendiéndose de los rayos UV.
    </p>
    <p>
      Mucha gente cree que las camas de bronceado no son dañinas, sin embargo
      esto es falso. El uso de las camas de bronceado han estado relacionadas
      con mayor riesgo de contraer melanoma, especialmente si comienzan a
      utilizarlas antes de los 30 años. La mayoría de los dermatólogos y
      organizaciones de salud no recomiendan el uso de las camas de bronceado y
      lámparas solares.
    </p>
    <p>
      <img src="/images/prevencion-6.png" loading="lazy" />
    </p>
    <h3>Protege a los niños del sol en el exterior.</h3>
    <p>
      Los niños necesita atención especial, pues normalmente pasan gran parte
      del tiempo al aire libre y no son conscientes del peligro. Los padres
      deben proteger a los niños de la excesiva exposición al sol y deben
      enseñarles a cuidarse desde pequeños.
    </p>
    <p>
      Debes crear el hábito de aplicar bloqueador solar en ti y en tus hijos
      antes de ir al exterior y cuando tengas planeado pasar un largo tiempo en
      el sol.
    </p>
    <p>
      Los niños deben aprender sobre el peligro que genera la prolongada
      exposición al sol desde pequeños, para que cuando sean más independientes,
      puedan protegerse ellos mismos.
    </p>
    <p>
      Los bebés menores a los 6 meses, deben estar alejados de la exposición del
      sol directa, deben utilizar sombreros y ropa protectora como barrera
      contra los rayos UV.
    </p>
    <p>
      <img src="/images/prevencion-7.png" loading="lazy" />
    </p>
    <h3>Conoce tu piel e identifica lunares o manchas sospechosas</h3>
    <p>
      Revisar tu piel regularmente ( una vez al mes) puede ayudar a detectar a
      tiempo cualquier nuevo lunar o lunares sospechosos, así como cualquier
      crecimiento anormal en tu cuerpo. Revisa cada zona de tu cuerpo, desde la
      cabeza hasta los pies.
    </p>
    <p>
      <img src="/images/prevencion-8.png" loading="lazy" />
    </p>
    <h3>
      Visita a un dermatólogo una vez al año, o en cuanto tengas alguna sospecha
      de algún lunar o mancha nueva o existente y que ha cambiado.
    </h3>
    <p>
      Las visitas de rutina con un dermatólogo y la auto exploración de la piel
      son recomendadas para todos.
    </p>
    <p>
      <img src="/images/prevencion-9.png" loading="lazy" />
    </p>
    <h3>Utiliza bloqueador solar diariamente</h3>
    <p>
      El protector o bloqueador solar es un producto que se aplica en la piel
      con el objetivo de portejerla contra los rayos UV, sin embargo, es
      importante saber que es un filtro que no bloquea el 100% de los rayos
      solares y esto también depende de la forma de aplicación. Por esta razón
      el bloqueador solar no debe utilizarse como único método de protección,
      este solo es uno de varios métodos que protegen la piel.
    </p>
    <p>
      Los protectores solares pueden ser físicos o minerales y vienen en
      diferentes formas y presentaciones como: cremas, lociones. geles, sprays,
      etc. Existen maquillajes que contiene un factor de protección, sin
      embargo, en su mayoría no son considerados protectores solares.
    </p>
    <p>
      Las 3 cosas en las que debes fijarte antes de comprar un protector solar
      son:
    </p>
    <ul>
      <li>Que tenga un Factor de Protección Solar (FPS) mayor o igual a 30.</li>
      <li>
        Que sea un protector de amplio espectro y proteja contra los rayos UVA y
        UVB.
      </li>
      <li>Que sea resistente al agua y al sudor.</li>
    </ul>
    <p>
      Ya que tienes un buen bloqueador es importante aplicarlo bien, para
      asegurar su adecuada protección. Siempre debes seguir las instrucciones de
      las etiquetas de los bloqueadores, pues indican la cantidad exacta que
      debes aplicar.
    </p>
    <p>
      Al aplicar el bloqueador debes tomar en cuenta la cara, las orejas, el
      cuello, los brazos y cualquier otra área que no esté cubierta por la ropa.
      No olvides los labios, también debes aplicar crema de labios con protector
      solar. El bloqueador se debe aplicar 30 minutos antes de salir al sol.
    </p>
    <p>
      Si vas a utilizar repelente de mosquitos o maquillaje, debes aplicar el
      protector solar antes.
    </p>
    <p>
      Idealmente debes aplicar 29 ml ( 2 cucharadas o la palma de la mano) de
      bloqueador cubriendo los brazos, las piernas, el cuello y la cara. Se debe
      volver a aplicar bloqueador cada 2 horas para una protección efectiva.
    </p>
    <p>
      El bloqueador se puede desvanecer cuando sudas o nadas, en estos casos
      debes re-aplicar constantemente.
    </p>
    <p>
      Algunas personas creen que si utilizan un protector solar con FPS muy
      alto, no es necesario aplicar mucho, o re aplicarlo y también su efecto
      dura por más tiempo, sin embargo, esto es falso.
    </p>
    <p>
      Si quieres broncearte, la opción más saludable es hacerlos a través de los
      bronceados artificiales como las cremas o los sprays que broncean después
      de aplicarse, sin embargo, si esto se hace frecuentemente, puede evitar
      que notes algún cambio en tu piel.
    </p>
    <p>
      Las lamparas pequeñas de UV, las cuales se utilizan en salones de belleza
      producen rayos UVA. La cantidad es mucho menor en comparación a una cama
      de bronceado, sin embargo una recomendación es aplicar bloqueador en las
      manos antes de utilizar ese tipo de lámpara.
    </p>
    <p>
      <img src="/images/prevencion-10.png" loading="lazy" />
    </p>
    <h3>Efectos secundarios de medicinas.</h3>
    <p>
      Algunas medicines pueden provocar mayor sensibilidad en la piel, cuando
      estas expuesto al sol, como pueden ser algunos antibióticos,
      anti-inflamatorios, anti-hongos, medicinas para la presión de la sangre y
      algunos tipos de quimioterapias.
    </p>
    <p>La detección temprana empieza en ti. Nadie conoce mejor tu cuerpo.</p>
    <p>
      Utilizar protección solar desde una edad pequeña es la defensa más potente
      contra el cáncer de piel. También es importante realizarte auto chequeos
      mensuales de la piel, y visitar a un dermatólogo una vez al año o más
      frecuentemente si ellos lo aconsejan.
    </p>
