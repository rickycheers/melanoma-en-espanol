    <h1>¿Qué es el Melanoma?</h1>
    <p>
      El <span class="underline">melanoma</span>, <span class="underline">melanoma maligno</span> o también conocido como <span class="underline">melanoma cutáneo</span>, es
      un tipo de cáncer de piel que se origina debido al crecimiento desmedido
      de los melanocitos, células que producen melanina, la cual se encarga de
      darle color a la piel, a los ojos y al cabello.
    </p>
    <p>
      Este tipo de cáncer de piel, es el menos común, sin embargo es el más
      agresivo ya que puede esparcirse a otros órganos rápidamente haciendo
      metástasis si no es tratado a tiempo. Cuando el melanoma hace metástasis
      en cualquier órgano como por ejemplo el hígado, el cáncer sigue llamándose
      melanoma aunque esté en el hígado, pues el cáncer se nombra a partir del
      lugar en donde se originó.
    </p>
    <p>
      La mayoría de las veces el melanoma aparece en la piel ( cara, cuello,
      pecho, espalda, hombros, piernas, manos, cabeza, pies, etc.), sin embargo,
      también puede aparecer en lugares que no necesariamente tienen tanta
      exposición al sol como son: los ojos, la boca, los genitales, debajo de
      las uñas y las plantas de los pies.
    </p>
    <p>
      En muchas ocasiones las células de melanoma siguen produciendo melanina,
      por lo tanto los tumores usualmente son cafés o negros, sin embargo,
      algunos melanomas no producen melanina por lo tanto pueden ser rosados o
      blancos
    </p>
    <p>
      A diferencia de otros tipos de cáncer, la mayoría de los tipos de melanoma
      se observan en la piel, lo cual puede llegar a facilitar la detección
      temprana de esta enfermedad, por esta razón es muy importante conocer
      nuestra piel y checarla periódicamente.
    </p>
