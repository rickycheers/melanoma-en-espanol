    <h1>Detección Temprana del Melanoma.</h1>
    <p>
      Cambios en la apariencia de la piel, son indicadores importantes de que
      puedes tener melanoma, es decir, el primer signo de melanoma es usualmente
      un cambio en el tamaño, forma, color o sensación en un lunar o mancha
      existente, o con la aparición de uno nuevo.
    </p>
    <p>
      Mientras que la mayoría de los melanomas primarios aparecen en la piel y
      son visibles, existe la posibilidad de poder detectar la enfermedad a
      tiempo y en etapa temprana, antes de que esta crezca y comience a
      esparcirse.
    </p>
    <p>
      Esto es importante porque cuando el melanoma es detectado a tiempo, la
      probabilidad de una esperanza de vida larga es mayor. Conforme el melanoma
      va avanzando, se vuelve más complicado de tratar y la enfermedad tiene un
      peor pronóstico.
    </p>
    <p>
      Cuando el melanoma es detectado en una etapa temprana, la probabilidad de
      sobrevivencia por los siguientes 5 años es del 92% al 97%. El 86% de los
      pacientes diagnosticados a tiempo, pueden disfrutar de una vida larga
      después de una sencilla cirugía.
    </p>
    <h2>
      ¿Qué hacer para detectar a tiempo el melanoma?
    </h2>
    <h3>Conoce tu piel y revísala regularmente.</h3>
    <p>
      Conocer tu piel y reconocer cambios en ella, es el mejor camino para poder
      detectar el melanoma a tiempo. Revisa tu piel de pies a cabeza, una vez al
      mes y vuelve esta actividad parte de tu rutina.
    </p>
    <p>
      Familiarizate con los lugares en donde tienes lunares y manchas, observa
      sus formas, tamaños, colores, etc. Ningún doctor puede conocer tu piel
      mejor que tú. Puedes tomar notas de los lunares sospechosos o
      características inusuales, esto para no olvidarlo.
    </p>
    <p>
      Cuando revises tu piel, debes hacerlo en un lugar con suficiente luz para
      poder observar con mayor claridad. Puedes ayudarte de espejos para
      alcanzar a ver aquellas zonas difíciles de ver como la espalda o atrás de
      las piernas.
    </p>
    <p>
      Examina todas las zonas de tu cuerpo, incluyendo las plantas de los pies,
      las palmas de las manos, las uñas, los ojos, orejas, el rostro, cuello,
      torso, pecho, piernas, pompas, espalda, pies y cuero cabelludo, para este
      puedes ayudarte con un cepillo de cabello y una secadora de aire.Puedes
      pedirle ayuda a alguna persona de confianza, para observar aquellas zonas
      que son complicada para ti.
    </p>
    <p>
      The Skin Cancer Foundation, recomienda que cuando realicemos nuestro
      chequeo mensual de la piel, siempre tengamos en la mente tres cosas al
      momento de observar nuestra piel, pues pueden ser señales de alarma.
    </p>
    <ul>
      <li>
        Busca lunares, manchas o lesiones nuevas, especialmente si aparecen
        después de los 21 años.
      </li>
      <li>
        Busca lunares o manchas que hayan cambiado, ya sea de color, forma,
        tamaño o textura.
      </li>
      <li>
        Observa lunares o manchas que sean inusuales, es decir, que duelan,
        piquen, den comezón, sangren o que formen costra y no sanen después de 3
        semanas.
      </li>
    </ul>
    <h3>
      No olvides la regla del
      <span class="text-skin-dark-400 font-bold">ABCDE</span> del Melanoma.
    </h3>
    <p>
      Siempre que revises tu piel ten en mente las características del
      <a
        class="text-skin-dark-400 font-bold"
        href="/documentos/ABCDE DEL MELANOMA.pdf"
        target="_blank"
        >ABCDE</a
      >
      del Melanoma, para que así puedas identificar fácilmente aquellos lunares
      o manchas con estas características y de esta forma puedas monitorearlos e
      informar sobre estas características a tu dermatólogo.
    </p>
    <ul>
      <li>
        Chequeos periódicos con tu doctor cuando tienes uno o varios factores de
        riesgo del melanoma, por lo menos una vez al año, como por ejemplo:
      </li>
      <li>Tener piel blanca, ojos claros, cabello rubio o rojizo</li>
      <li>
        Tener historial de varias quemaduras por la exposición al sol
      </li>
      <li>Tener muchos lunares o lunares atípicos en el cuerpo o lunares</li>
      <li>
        Tener antecedentes familiares y/o personales de cáncer de piel o
        melanoma
      </li>
    </ul>
    <p>
      Si tu medico general observa algún lunar o mancha inusual, debe referirte
      con un dermatólogo. Si en lo personal te preocupa alguna mancha o lunar
      nuevo o cambiante, visita directamente a un dermatólogo.
    </p>
    <p>
      Algunos dermatólogos utilizan un instrumento llamado dermatoscopio, el
      cual sirve para observar lunares o manchas de una forma más clara y nítida
      en comparación con el ojo. Es muy común que si el dermatólogo tiene
      sospecha de algún lunar, recomiende hacer una biopsia del lunar o mancha
      para posteriormente mandarlo a analizar con un patólogo y descartar que
      sea melanoma u otro tipo de cáncer de piel. También es común que un
      dermatólogo tome fotos de ciertos lunares con el objetivo de monitorearlos
      en visitas posteriores o tomar fotos del lunar antes de realizar una
      biopsia.
    </p>
    <h3>Mapeo de Lunares o Fotografias de Cuerpo Completo</h3>
    <p>
      Es muy probable que si tienes antecedentes de melanoma o tienes muchos
      lunares atípicos, tu dermatólogo recomendará que tomes fotos de todos tus
      lunares periódicamente para poderlos comparar y así detectar algún cambio
      en la forma, color o tamaño.
    </p>
    <p>
      Detectar el Melanoma a tiempo permite tener más opciones de tratamientos y
      menores costos. Algunos melanomas en etapa temprana pueden mostrar signos
      o síntomas que pueden ser notables, sin embargo, no siempre es el caso.
    </p>
    <h3>Seguir La Regla del Patito Feo</h3>
    <p>
      La regla del
      <a href="/documentos/LA REGLA DEL PATITO FEO.pdf" target="_blank"
        >patito feo</a
      >
      es un método utilizado para detectar a tiempo el melanoma, junto con la
      regla del <span class="text-skin-dark-400 font-bold">ABCDE</span> del
      melanoma. Es una herramienta recomendada para todos aquellos que tienen
      uno o varios factores de riesgo del melanoma.
    </p>
    <p>
      Esta regla se basa en el hecho de que los lunares normales se parecen
      entre si, y el melanoma normalmente se ve distinto comparado con el resto
      de los lunares o manchas que se encuentran en esa misma área de nuestra
      piel.
    </p>
    <p>
      La importancia de esta regla se basa en que no sólo debemos revisar
      nuestra piel buscando irregularidades, también debemos comparar cualquier
      mancha o lunar sospechosos que tengamos, con el resto de los lunares que
      se encuentren en la misma zona, siempre tomando en cuenta tres
      características:
    </p>
    <ul>
      <li>Un lunar o mancha que es más grande o más pequeña que el resto</li>
      <li>
        Un lunar o mancha que es más clara u obscura en comparación al resto
      </li>
      <li>
        Lesiones o manchas las cuales no estén rodeadas de lunares, es decir,
        lesiones o manchas que se encuentren solas.
      </li>
    </ul>
    <p>
      El tener alguna de estas características puede ser una señal de alarma,
      aunque no necesariamente significa que es melanoma. Simplemente es una
      herramienta que puede ayudarte a identificar manchas o lunares
      sospechosos, los cuales deberán ser revisados por un dermatólogo
      posteriormente para descartar cualquier tipo de cáncer de piel, incluyendo
      el melanoma.
    </p>
    <h3>Confía en tu instinto cuando te dice que algo esta mal</h3>
    <p>
      El melanoma es muy peligroso una vez que se diagnostica en etapa avanzada,
      es por esto que vale la pena seguir tu instinto y visitar a tu doctor
      cuando tienes alguna duda o preocupación respecto a tu piel. Más vale
      prevenir que lamentar.
    </p>
    <h3>Si tuviste melanoma anteriormente</h3>
    <p>
      No olvides visitar periódicamente a tu oncólogo y dermatólogo, una vez que
      terminaste el tratamiento. Es importante que te tengan monitoreado para
      prevenir o detectar a tiempo cualquier recurrencia de la enfermedad.
    </p>
    <p>
      La mayoría de los lunares o manchas en la piel son inofensivas, pero no
      siempre. El melanoma no siempre cumple las características o signos de
      alerta, esta enfermedad puede verse de diferentes formas, por esa razón es
      importante estar al pendiente de cualquier cambio o aspecto nuevo en tu
      piel, ya sea en la que esta expuesta constantemente al sol o la que no lo
      esta.
    </p>
    <p>
      El melanoma puede curarse, siempre y cuando sea detectado en etapa
      temprana. Algunas personas tiene mayor riesgo de tener esta enfermedad en
      comparación a otras, sin embargo nadie esta exento a padecerlo, el simple
      hecho de tener piel te hace candidato a poder tener melanoma.
    </p>
