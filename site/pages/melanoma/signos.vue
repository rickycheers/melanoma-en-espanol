    <h1>Signos del Melanoma.</h1>
    <p>
      Los lunares, llagas, protuberancias, manchas, marcas o cambios inusuales
      en la apariencia o sensación de un área de la piel pueden ser un signo de
      melanoma u otro tipo de cáncer de piel, o una advertencia de que podría
      convertiste en cáncer de piel.
    </p>
    <p>
      Los primeros signos y síntomas frecuentemente son:
    </p>
    <ul>
      <li>Cambios en lunares ya existentes</li>
      <li>
        Desarrollo de un nuevo lunar, mancha o un aspecto inusual en la piel.
      </li>
    </ul>
    <p>
      Pero el melanoma no siempre comienza a partir de un lunar, también puede
      ocurrir en piel con apariencia normal, incluso en manchas en los ojos, o
      en algún lugar como la boca, nariz o genitales. Asegúrate de visitar a tu
      doctor regularmente y comentarle tus preocupaciones cuando veas algún
      cambio en tu cuerpo inusual como los anteriores ya mencionados.
    </p>
    <h3>Lunares Normales.</h3>
    <p>
      Los lunares normales generalmente son de un color uniforme, ya sea
      bronceado, cafe o negro, y tienen un borde distintivo que separa el lunar
      de la piel circundante. Normalmente son ovalados o redondos y por lo
      general, tienen un diámetro menor a los 6 mm (tamaño de un borrador de
      lápiz).
    </p>
    <p>
      La mayoría de los lunares aparecen en la infancia, sin embargo pueden
      formarse lunares nuevos hasta aproximadamente los 40 años. Tener lunares
      nuevos a partir de los 21 años, puede ser un signo de alerta, aunque no
      necesariamente sea algo malo.
    </p>
    <h3>Lunares Inusuales o Atípicos que Pueden Indicar Melanoma.</h3>
    <p>
      Para ayudarte a identificar las características de lunares inusuales que
      pudieran indicar melanoma u otro tipo de cáncer de piel, debes conocer la
      regla del <strong>ABCDE del melanoma</strong>.
    </p>
    <ul>
      <li>
        <strong>A</strong> - significa que la forma del lunar es asimétrico, es
        decir, que tiene una forma irregular, es decir, si partieras el lunar a
        la mitad, notarías que cada mitad es diferente a la otra, en cuanto a su
        forma.
      </li>
      <li>
        <strong>B</strong> - significa que el lunar tiene bordes irregulares, es
        decir, que los bordes no son definidos.
      </li>
      <li>
        <strong>C</strong> - se refiere al cambio de color, este tipo de lunares
        tienen más de un color. Busca manchas o lunares que tengan muchos
        colores o una distribución desigual del color.
      </li>
      <li>
        <strong>D</strong> - se refiere al diámetro del lunar, es cuando el
        diámetro es igual o mayor a los 6mm. Una referencia fácil de recordar es
        el tamaño de la goma de un lápiz, cuando el lunar es mayor a esta,
        significa que rebasa los 6mm.
      </li>
      <li>
        <strong>E</strong> - esta letra se refiere a la evolución o cambio del
        lunar o mancha. Fíjate en cambios de forma, color, textura, o tamaño del
        lunar a través del tiempo. Los lunares también pueden cambiar y
        desarrollar nuevos síntomas o signos como comezón y sangrado.
      </li>
    </ul>
    <p>
      Los lunares malignos o cancerosos pueden variar en apariencia, según el
      tipo de cáncer de piel. Algunos pueden tener todas las características
      anteriormente mencionadas ,sin embargo, otros pueden tener solamente una o
      dos características inusuales, por esta razón es importante mencionarle a
      tu Doctor todos aquellos cambios y nuevos lunares, manchas o crecimientos
      en la piel que hayas notado.
    </p>
    <p>Otros signos de alerta pueden ser:</p>
    <ul>
      <li>
        Una herida que no sana Propagación del color desde el borde de una
        mancha o lunar, hasta la piel que lo rodea.
      </li>
      <li>
        Enrojecimiento o hinchazón más allá del borde de un lunar existente.
      </li>
      <li>
        Cambios en la sensación, como picazón, sensibilidad o dolor en un lunar
        o mancha. Cambio en la superficie de un lunar: descamación, exudación,
        sangrado o la aparición de un bulto o protuberancia
      </li>
    </ul>
    <p>
      En ocasiones es difícil de reconocer la diferencia entre un lunar normal y
      el melanoma incluso para los doctores, por esta razón es importante que
      cualquier duda que tengas la comuniques lo antes posible con los
      especialistas para que ellos se encarguen de realizar los debidos
      procedimientos para asegurar que no es nada malo.
    </p>
    <h3>Melanomas Escondidos.</h3>
    <p>
      El melanoma también puede desarrollarse en áreas del cuerpo que no han
      sido expuestas al sol o a los rayos UV, como puede ser, los ojos, debajo
      de las uñas, entre los dedos de los pies o manos, las palmas de las manos,
      plantas de los pies, cuero cabelludo, boca, nariz y genitales. Incluso
      puede aparecer en el tracto digestivo y tracto urinario.
    </p>
    <p>
      Los anteriores son algunos ejemplos de los tipos de melanoma que pueden
      llegar a ser difíciles de detectar, ya que normalmente no revisamos esas
      áreas de nuestro cuerpo. Es más común encontrar este tipo de melanomas en
      personas de piel obscura.
    </p>
