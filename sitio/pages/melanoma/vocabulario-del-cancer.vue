<template>
  <article class="prose max-w-none">
    <h1>Vocabulario del Cáncer</h1>
    <dl>
      <dt>Cáncer:</dt>
      <dd>
        El cáncer es una enfermedad que se puede originar en casi cualquier
        parte del cuerpo. Comienza cuando las células crecen descontroladamente
        sobrepasando el tamaño de las células normales, lo cual dificulta que el
        cuerpo funcione de la manera que debería. Existen muchos tipos de
        cáncer, pues no es una sola enfermedad, ya que puede originarse en
        distintas partes del cuerpo, como los pulmones, los seno, el hígado, la
        piel, la sangre, entre otros.
      </dd>
      <dt>Tumores:</dt>
      <dd>
        La mayoría de los cánceres forman una masa a la cual se le denomina
        tumor o crecimiento. No todas las masas (tumores) son cancerosas.
      </dd>
      <dt>Benigno:</dt>
      <dd>Tumor (masa o protuberancia) que no es canceroso.</dd>
      <dt>Maligno:</dt>
      <dd>Tumor que indica presencia de células cancerosas.</dd>
      <dt>Biopsia:</dt>
      <dd>
        Procedimiento médico que consiste en extraer una porción de tejido para
        analizar si contiene células cancerosas.
      </dd>
      <dt>Metástasis:</dt>
      <dd>
        Es la propagación de las células cancerosas hacia otras partes del
        cuerpo por medio del sistema linfático o del torrente sanguíneo.
      </dd>
      <dt>Micrometástasis:</dt>
      <dd>
        Metástasis que son demasiado pequeñas para ser vistas en estudios de
        imágenes.
      </dd>
      <dt>Oncólogo:</dt>
      <dd>Doctor que se especializa en tratar enfermedades de cáncer.</dd>
      <dt>Patólogo:</dt>
      <dd>
        Médico que se especializa en interpretar análisis de laboratorio y
        evaluar células, tejidos y órganos para diagnosticar enfermedades.
      </dd>
      <dt>Quimioterapia:</dt>
      <dd>Tipos de medicamentos utilizados para tratar distintos tipos de cáncer.</dd>
      <dt>Radioterapia:</dt>
      <dd>
        Uso de rayos de alta energía, como los rayos X, para tratar algunos
        tipos de cáncer.
      </dd>
      <dt>Inmunoterapia o terapia biológica:</dt>
      <dd>
        La inmunoterapia es un método para tratar el cáncer que utiliza las
        capacidades propias del cuerpo para identificar y matar las células
        cancerosas. Los tratamientos de inmunoterapia se pueden utilizar para
        atacar las células cancerosas directamente, para estimular el sistema
        inmunológico para que destruya las células cancerosas o para evitar que
        el cáncer regrese.
      </dd>
      <dt>Terapia adyuvante:</dt>
      <dd>
        Es el tratamiento que se administra después del tratamiento principal
        para reducir la probabilidad de recurrencia del cáncer al destruir
        cualquier resto de células cancerosas. Se suele referir a la
        quimioterapia, la radioterapia, la terapia hormonal y/o la inmunoterapia
        que se administran después de la cirugía.
      </dd>
      <dt>Terapia neoadyuvante:</dt>
      <dd>
        Es el tratamiento que se administra antes del tratamiento principal.
        Puede incluir quimioterapia, radioterapia o terapia hormonal que se
        administran antes de la cirugía para reducir el tamaño de un tumor y
        simplificar la extirpación.
      </dd>
      <dt>Remisión:</dt>
      <dd>
        Es cuando los signos o síntomas del cáncer han desaparecido parcial o
        totalmente. En la remisión parcial, algunos de los signos y síntomas de
        cáncer han desaparecido. En remisión completa, todos los signos y
        síntomas de cáncer han desaparecido, aunque el cáncer todavía puede
        estar en el cuerpo.
      </dd>
      <dt>NED:</dt>
      <dd>
        El término “sin evidencia de enfermedad” se usa a menudo con el cáncer
        cuando no hay evidencia física de la enfermedad en el examen o en las
        pruebas de imagen después del tratamiento.
      </dd>
      <dt>Recurrencia:</dt>
      <dd>
        El cáncer que ha vuelto a aparecer después de un período en el que no se
        podía detectar.
      </dd>
      <dt>Etapa o Fases:</dt>
      <dd>
        Término usado para determinar si el cáncer se ha propagado, y de ser el
        caso, qué tanto lo ha hecho. Una etapa 1 o 2, significa que el cáncer no
        se ha propagado o no tanto. Mientras que una etapa 3 o 4, significa una
        mayor propagación. La etapa 4 es la mayor.
      </dd>
      <dt>Sistema linfático:</dt>
      <dd>
        Son redes de pequeños vasos, conductos y órganos que transportan fluidos
        hacia y desde el torrente sanguíneo y los tejidos corporales. El cáncer
        puede extenderse hacia otras partes del cuerpo por el sistema linfático.
      </dd>
      <dt>PET:</dt>
      <dd>
        La tomografía por emisión de positrones es uno de los procedimientos más
        utilizados dentro de la imagen molecular. La imagen molecular es una
        técnica de diagnóstico por la imagen que proporciona imágenes muy
        detalladas a cerca del funcionamiento molecular y celular del interior
        del cuerpo humano. Identificar enfermedad en sus fases más iniciales y
        determinar la ubicación exacta del cáncer, a menudo antes de que
        proporcione síntomas o antes de que otras anormalidades puedan ser
        detectadas mediante otras pruebas diagnósticas.
      </dd>
    </dl>
